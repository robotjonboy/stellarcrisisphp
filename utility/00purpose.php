Utility programs are used to set up the game or to correct errors.
They are located in the utility directory.
There are also fixes. These programs are run from the command line and have no
web interface from the administration button on the web page.
They are slighlty dangerous programs.

The admin directory contains programs used by the administrator once the game has been set up.
These programs are all accessable from the administration button on the web page.
The programs are for a normally working game system.
